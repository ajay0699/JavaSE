public class vowel
{
	public static void main(String args[])
	{
		char A='c';
		switch(A)
		{
		case 'a':
		System.out.println("A");
		break;
		case 'e':
		System.out.println("E");
		break;
		case 'i':
		System.out.println("I");
		break;
		case 'o':
		System.out.println("O");
		break;
		case 'u':
		System.out.println("u");
		break;
		default:
		System.out.println("Not a Vowel");
		}
		

	}
}